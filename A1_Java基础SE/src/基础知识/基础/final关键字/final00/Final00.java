/*
final关键字代表最终、不可改变的。

常见的四种用法:
1. 可以修饰一个类
2. 可以修饰一个方法
3. 可以修饰一个局部变量
4. 可以修饰一个成员变量

*/

//修饰一个类
public final class Final00 {

    //修饰一个成员方法
    public final int num = 10;

    //修饰一个方法
    public final void method(){
        //修饰一个局部变量
        final int num = 20;
    }





}
