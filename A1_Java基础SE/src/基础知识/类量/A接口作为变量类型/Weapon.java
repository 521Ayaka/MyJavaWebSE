package 基础知识.类量.A接口作为变量类型;

// weapon 武器
public class Weapon {

    private String code;

    public Weapon() {
    }

    public Weapon(String code) {
        this.code = code;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

}
