package 基础知识.类库.ArrayMethod;

public class Lei {
    private int i;
    private String str;
    private boolean v;

    public Lei() {
    }
    public Lei(int i, String str, boolean v) {
        this.i = i;
        this.str = str;
        this.v = v;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public boolean isV() {
        return v;
    }

    public void setV(boolean v) {
        this.v = v;
    }
}
