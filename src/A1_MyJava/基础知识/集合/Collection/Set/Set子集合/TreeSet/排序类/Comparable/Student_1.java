public class Student_1 {

    private String name;
    private int age;

    public Student_1(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public Student_1() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
